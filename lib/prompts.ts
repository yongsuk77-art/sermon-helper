function contextBlock(ctx: SermonContext): string {
  const lines: string[] = [`• 본문: ${ctx.passage}`];
  if (ctx.theme?.trim()) lines.push(`• 설교 주제/방향: ${ctx.theme.trim()}`);
  if (ctx.audience?.trim()) lines.push(`• 청중: ${ctx.audience.trim()}`);
  if (ctx.occasion?.trim()) lines.push(`• 절기/상황: ${ctx.occasion.trim()}`);
  if (ctx.notes?.trim()) lines.push(`• 설교자 메모: ${ctx.notes.trim()}`);
  return lines.join("\n");
}

