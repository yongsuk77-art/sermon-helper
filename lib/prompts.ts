export function buildMessages(mode: ModeId, ctx: SermonContext) {
  const user = `${MODE_INSTRUCTIONS[mode]}

---
연구 대상 정보:
${contextBlock(ctx)}
---

위 본문에 대해 한국어 마크다운으로 작성하세요.`;

  return {
    system: BASE_SYSTEM,
    user,
  };
}

export function buildQaMessages(
  ctx: SermonContext,
  question: string,
  priorResults: string,
) {
  const system = `${BASE_SYSTEM}

설교자가 본문 연구 중 자유롭게 묻는 질문에 답합니다. 본문의 맥락에 충실하게, 구체적이고 실질적으로 답하세요. 필요하면 원어·번역·배경·적용을 아울러 설명합니다.`;

  const user = `연구 대상 정보:
${contextBlock(ctx)}
${priorResults ? `\n참고 — 이미 생성된 연구 자료 요약(맥락용):\n${priorResults}\n` : ""}
---
설교자의 질문:
${question}

위 질문에 한국어 마크다운으로 답하세요.`;

  return { system, user };
}
