export function buildWorksheet(mode: ModeId, ctx: SermonContext) {
  const meta = MODES.find((item) => item.id === mode);
  return `# ${meta?.icon ?? ""} ${meta?.label ?? "설교 준비 워크시트"} — 직접 연구 양식

> AI를 호출하지 않는 무료 양식입니다. 먼저 성경과 신뢰할 수 있는 자료로 직접 채운 뒤, 필요한 단계만 AI로 보강하면 더 안전하고 경제적입니다.

${contextSummary(ctx)}

---

${WORKSHEETS[mode]}`;
}

export function buildWorksheetSet(ctx: SermonContext) {
  return MODES.reduce<Partial<Record<ModeId, string>>>((acc, mode) => {
    acc[mode.id] = buildWorksheet(mode.id, ctx);
    return acc;
  }, {});
}
